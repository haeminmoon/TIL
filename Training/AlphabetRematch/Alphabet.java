package AlphabetRemetch;

public class Alphabet {
	char word;
	int ascii;
	int count;
	
	public char getWord() {
		return word;
	}
	public void setWord(char word) {
		this.word = word;
	}
	public int getAscii() {
		return ascii;
	}
	public void setAscii(int ascii) {
		this.ascii = ascii;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
}
